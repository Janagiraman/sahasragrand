<?php

namespace App\Http\Controllers;

use Illuminate\Http\Request;
use App\Posting;

class PostingController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $postings = Posting::paginate(5);
        return view('postings.index',compact(['postings']));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        //
        return view('postings.create');
    
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $request->validate([
            'name' => 'required|unique:postings,name',
        ]);
        $posting = new Posting();
        $posting->name = $request->input('name');
        $posting->save();
        return redirect( route('postings.index'))->withSuccess('Posting added successfully!');
        
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $posting = Posting::find($id);
        return view('postings.edit',compact(['posting']));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $request->validate([
            'name' => 'required|unique:postings,name,'.$id,
        ]);
        $posting = Posting::find($id);
        $posting->name = $request->input('name');
        $posting->save();
        return redirect( route('postings.index'))->withSuccess('Posting Updated Successfully!');
    }

